package swig.lecture.model;

public class LectureTimeModel {
	private int lt_no;
	private int ld_no;
	private String lt_time;
	private String lt_time2;
	
	public int getLt_no() {
		return lt_no;
	}
	public void setLt_no(int lt_no) {
		this.lt_no = lt_no;
	}
	public int getLd_no() {
		return ld_no;
	}
	public void setLd_no(int ld_no) {
		this.ld_no = ld_no;
	}
	public String getLt_time() {
		return lt_time;
	}
	public void setLt_time(String lt_time) {
		this.lt_time = lt_time;
	}
	public String getLt_time2() {
		return lt_time2;
	}
	public void setLt_time2(String lt_time2) {
		this.lt_time2 = lt_time2;
	}
}
