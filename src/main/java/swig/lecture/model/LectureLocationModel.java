package swig.lecture.model;

public class LectureLocationModel {
	private int ll_no;
	private int l_no;
	private String ll_location;
	private String ll_detail;
	private String ll_addcost;
	private String ll_lo_comment;
	
	public int getLl_no() {
		return ll_no;
	}
	public void setLl_no(int ll_no) {
		this.ll_no = ll_no;
	}
	public int getL_no() {
		return l_no;
	}
	public void setL_no(int l_no) {
		this.l_no = l_no;
	}
	public String getLl_location() {
		return ll_location;
	}
	public void setLl_location(String ll_location) {
		this.ll_location = ll_location;
	}
	public String getLl_detail() {
		return ll_detail;
	}
	public void setLl_detail(String ll_detail) {
		this.ll_detail = ll_detail;
	}
	public String getLl_addcost() {
		return ll_addcost;
	}
	public void setLl_addcost(String ll_addcost) {
		this.ll_addcost = ll_addcost;
	}
	public String getLl_lo_comment() {
		return ll_lo_comment;
	}
	public void setLl_lo_comment(String ll_lo_comment) {
		this.ll_lo_comment = ll_lo_comment;
	}
}
